# 邮局问题 · Post Office Problem
# LintCode 版权所有
# 动态规划
# 描述
# There are n houses on a line. Given an array A and A[i] represents the position of i-th house.
# Now you need to pick k position to build k post offices.
#
# What is the minimum sum distance from these n houses to the nearest post office?
#
# All positions are integers.
# 样例
# Example 1:
#
# Input: A = [1, 2, 3, 4, 5], k = 2
# Output: 3
# Explanation: Build post offices on position 2 and 4.
# Example 2:
#
# Input: A = [1, 2], k = 1
# Output: 1
# Explanation: Build post office on position 1 or 2.
# 挑战
